        # 最終失敗
        assert result.diagram_image == b""
        assert "exhausted" in result.error or "regen" in result.error.lower() or result.error
        # Agent 被呼叫 2 次（MAX_AGENT_REGEN_RETRIES=2）
        assert mock_agents_mod.invoke_agent_raw.await_count == 2
        # render 被呼叫 3 次（初次 + 2 次 regen 後）
        assert mock_render.await_count == 3


class TestGetAvailableAzureClassesSummary:
    def test_returns_string(self):
        result = get_available_azure_classes_summary()
        assert isinstance(result, str)

    def test_contains_module_paths(self):
        """應包含 diagrams.azure.* 的模組路徑。"""
        result = get_available_azure_classes_summary()
        # diagrams 套件已安裝時，至少會有 compute / network 等模組
        if "diagrams package not installed" not in result:
            assert "diagrams.azure" in result
            # 至少包含幾行
            lines = [l for l in result.strip().split("\n") if l.strip()]
            assert len(lines) >= 3, f"Expected multiple module lines, got {len(lines)}"

    def test_format_colon_separated(self):
        """每行格式為 'module_path: Class1, Class2, ...'。"""
        result = get_available_azure_classes_summary()
        if "diagrams package not installed" not in result:
            for line in result.strip().split("\n"):
                if line.strip():
                    assert ": " in line, f"Line missing ': ' separator: {line}"



def _make_success_render_result(code: str = "") -> DiagramOutput:
    """模擬渲染成功的 DiagramOutput。"""
    return DiagramOutput(
        diagram_py=code,
        diagram_image=b"\x89PNG_FAKE",
        diagram_image_ext="png",
        render_log="[Render] OK",
        status=StepStatus.SUCCESS,
    )


def _make_failed_render_result(code: str = "", error: str = "ImportError: cannot import name 'Foo'") -> DiagramOutput:
    """模擬渲染失敗的 DiagramOutput。"""
    return DiagramOutput(
        diagram_py=code,
        diagram_image=b"",
        render_log=f"[Render] FAILED: {error}",
        status=StepStatus.FAILED,
        error=error,
    )


def _make_regenerated_diag_output() -> DiagramOutput:
    """模擬 agent 重新產生的 DiagramOutput（已 parse）。"""
    return DiagramOutput(
        diagram_py='from diagrams import Diagram\nwith Diagram("regen"): pass',
        diagram_image=b"",
        render_log="regenerated by agent",
        approved_resource_manifest=ResourceManifest(),
        status=StepStatus.SUCCESS,
    )


def _make_mock_ctx() -> AsyncMock:
    """建立模擬 WorkflowContext。"""
    ctx = AsyncMock()
    ctx.yield_output = AsyncMock()
    return ctx


